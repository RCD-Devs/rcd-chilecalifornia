<script>
  const tabs = document.querySelectorAll('.tab-nuestro-impacto');
  const sections = document.querySelectorAll('.section-nuestro-impacto');

  tabs.forEach(tab => {
    tab.addEventListener('click', () => {
      tabs.forEach(t => t.classList.remove('active'));
      sections.forEach(s => s.classList.remove('active'));

      tab.classList.add('active');
      const sectionId = tab.dataset.section;
      const activeSection = document.getElementById(sectionId);
      activeSection.classList.add('active');

      if (window.innerWidth <= 768) {
        initSwiperInSection(activeSection);
      }

      setTimeout(() => {
        initWavesIn(activeSection);
      }, 100);
    });
  });

  // Inicia o reinicia las olas
  function initWavesIn(section) {
    section.querySelectorAll('.waves-nuestro-impacto').forEach((oldCanvas, index) => {
      if (oldCanvas._sineInstance && typeof oldCanvas._sineInstance.destroy === 'function') {
        oldCanvas._sineInstance.destroy();
      }

      const newCanvas = oldCanvas.cloneNode(true);
      oldCanvas.parentNode.replaceChild(newCanvas, oldCanvas);

      const canvasWidth = newCanvas.getBoundingClientRect().width || 300;

      newCanvas._sineInstance = new SineWaveGenerator({
        el: newCanvas,
        offsetX: index * canvasWidth,
        waves: [
          { speedModifier: 0.5, timeModifier: 2, lineWidth: 1, amplitude: 50, wavelength: 180, strokeStyle: 'rgba(145, 198, 155, 0.5)' },
          { speedModifier: 0.6, timeModifier: 2.5, lineWidth: 1, amplitude: 30, wavelength: 220, strokeStyle: 'rgba(88, 178, 199, 0.4)' },
          { speedModifier: 0.2, timeModifier: 1, lineWidth: 1, amplitude: 20, wavelength: 400, strokeStyle: 'rgba(145, 198, 155, 0.3)' }
        ]
      });
    });
  }

  // Swiper mobile
  function initSwiperInSection(section) {
    section.querySelectorAll('.cards-container-nuestro-impacto').forEach(container => {
      if (container.classList.contains('swiper')) return;

      const cards = Array.from(container.children);
      const wrapper = document.createElement('div');
      wrapper.classList.add('swiper-wrapper');

      cards.forEach(card => {
        const slide = document.createElement('div');
        slide.classList.add('swiper-slide');
        slide.appendChild(card);
        wrapper.appendChild(slide);
      });

      container.innerHTML = '';
      container.appendChild(wrapper);
      container.classList.add('swiper');

      const pagination = document.createElement('div');
      pagination.classList.add('swiper-pagination');
      container.parentElement.appendChild(pagination);

      new Swiper(container, {
        slidesPerView: 1,
        spaceBetween: 10,
        pagination: {
          el: pagination,
          clickable: true
        },
        on: {
          init: () => {
            setTimeout(() => {
              initWavesIn(container);
            }, 100);
          }
        }
      });
    });
  }

  // Resize dinámico: reinicia las olas o swiper si cambia de mobile a desktop
  function reinitializeOnResize() {
    let previousIsMobile = window.innerWidth <= 768;

    window.addEventListener('resize', () => {
      const isMobile = window.innerWidth <= 768;

      sections.forEach(section => {
        if (section.classList.contains('active')) {
          if (isMobile !== previousIsMobile) {
            if (isMobile) {
              initSwiperInSection(section);
            }
            setTimeout(() => initWavesIn(section), 100);
          } else {
            setTimeout(() => initWavesIn(section), 100);
          }
        }
      });

      previousIsMobile = isMobile;
    });
  }

  // SineWave Generator
  function SineWaveGenerator(options) {
    Object.assign(this, options || {});
    if (!this.el) throw "No Canvas Selected";
    this.ctx = this.el.getContext('2d');
    if (!this.waves.length) throw "No waves specified";

    this._resizeWidth();
    this._destroyed = false;

    this._loopId = requestAnimationFrame(this.loop.bind(this));
  }

  SineWaveGenerator.prototype._resizeWidth = function () {
    this.dpr = window.devicePixelRatio || 1;
    const wrapper = this.el.parentElement;

    this.width = this.el.width = this.el.offsetWidth * this.dpr;
    this.height = this.el.height = wrapper.offsetHeight * this.dpr;

    this.el.style.width = this.el.offsetWidth + 'px';
    this.el.style.height = wrapper.offsetHeight + 'px';
  };

  SineWaveGenerator.prototype.clear = function () {
    this.ctx.clearRect(0, 0, this.width, this.height);
  };

  SineWaveGenerator.prototype.drawSine = function (time, options) {
    const amplitude = options.amplitude || 50;
    const wavelength = options.wavelength || 50;
    const lineWidth = options.lineWidth || 2;
    const strokeStyle = options.strokeStyle || 'rgba(145, 198, 155, 1)';
    const segmentLength = options.segmentLength || 10;

    this.ctx.lineWidth = lineWidth * this.dpr;
    this.ctx.strokeStyle = strokeStyle;
    this.ctx.beginPath();

    const yAxis = this.height / 2;
    this.ctx.moveTo(0, yAxis);

    for (let i = 0; i <= this.width; i += segmentLength) {
      const x = (time * options.speedModifier) + (i + (this.offsetX || 0)) / wavelength;
      const y = Math.sin(x) * amplitude;
      this.ctx.lineTo(i, yAxis + y);
    }

    this.ctx.stroke();
  };

  SineWaveGenerator.prototype.loop = function () {
    if (this._destroyed) return;
    this.clear();
    const time = Date.now() / 3000;
    this.waves.forEach(wave => {
      this.drawSine(time * wave.timeModifier, wave);
    });
    this._loopId = requestAnimationFrame(this.loop.bind(this));
  };

  SineWaveGenerator.prototype.destroy = function () {
    this._destroyed = true;
    cancelAnimationFrame(this._loopId);
    this.clear();
  };

  // Al cargar
  document.addEventListener('DOMContentLoaded', () => {
    const initialSection = document.querySelector('.section-nuestro-impacto.active');

    if (initialSection) {
      if (window.innerWidth <= 768) {
        initSwiperInSection(initialSection);
      }

      setTimeout(() => {
        initWavesIn(initialSection);
      }, 100);
    }

    reinitializeOnResize();
  });
